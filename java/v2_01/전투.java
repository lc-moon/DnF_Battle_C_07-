// 전투.java
package 게임;

public class 전투 {
    private 플레이어 플레이어검증기 = new 플레이어();

    public 캐릭터 캐릭터생성(String 플레이어id, String 캐릭터명, String 직업, int 레벨) {
        if (!플레이어검증기.플레이어체크(플레이어id)) {
            return null; 
        }

        if ("전사".equals(직업)) {
            return new 전사(캐릭터명, 레벨);
        } else if ("마법사".equals(직업)) {
            return new 마법사(캐릭터명, 레벨);
        }
        return null;
    }

    public String 몬스터공격(String 플레이어id, 캐릭터 캐릭터객체) {
        if (!플레이어검증기.플레이어체크(플레이어id)) {
            return "인증 실패: 플레이어 ID가 올바르지 않습니다.";
        }
        if (캐릭터객체 == null) {
            return "공격 실패: 캐릭터가 존재하지 않습니다.";
        }

        String 스킬명 = 캐릭터객체.스킬발동();
        double 데미지 = 0;

        if (캐릭터객체 instanceof 전사) {
            데미지 = 캐릭터객체.get공격력() * 1.5;
        } else if (캐릭터객체 instanceof 마법사) {
            데미지 = 캐릭터객체.get공격력() * 2.0;
        }

        String 등급 = "";
        if (데미지 >= 200) {
            등급 = "S급 공격";
        } else if (데미지 >= 100) {
            등급 = "A급 공격";
        } else {
            등급 = "B급 공격";
        }

        return "스킬: [" + 스킬명 + "] | 데미지: " + 데미지 + " | 판정: " + 등급;
    }

    // ==========================================
    // 새로 추가된 비즈니스 로직 (순차 다이어그램 기반 연동)
    // ==========================================
    
    // UC4: 아이템획득 컨트롤 행위
    public String 아이템획득(String 플레이어id, String 아이템명, String 타입, int 가치, 캐릭터 캐릭터객체) {
        // 1. 필수 플레이어 체크 (Include 관계 실행)
        if (!플레이어검증기.플레이어체크(플레이어id)) {
            return "인증 실패: 플레이어 ID가 올바르지 않습니다.";
        }
        if (캐릭터객체 == null) {
            return "획득 실패: 대상 캐릭터 객체가 유효하지 않습니다.";
        }

        // 2. 캐릭터의 인벤토리 객체 획득 (get인벤토리멤버 호출)
        인벤토리 인벤 = 캐릭터객체.get인벤토리멤버();
        
        // 3. 인벤토리 수량 검증 (get아이템리스트크기 호출)
        if (인벤.get아이템리스트크기() >= 10) {
            return "인벤토리 초과: 인벤토리가 가득 차서 아이템을 획득할 수 없습니다.";
        }

        // 4. 아이템 가치에 따른 등급 부여 로직
        String 등급 = "일반(Common)";
        if (가치 >= 1000) {
            등급 = "전설(Legendary)";
        } else if (가치 >= 500) {
            등급 = "희귀(Rare)";
        }

        // 5. 새 아이템 생성 후 인벤토리에 추가 명령 위임 (아이템추가 호출)
        아이템 새아이템 = new 아이템(아이템명, 타입, 가치, 등급);
        boolean 추가결과 = 인벤.아이템추가(새아이템);

        if (!추가결과) {
            return "획득 실패: 시스템 오류로 아이템을 인벤토리에 넣지 못했습니다.";
        }

        return "아이템 획득 성공! | 명칭: " + 아이템명 + " | 타입: " + 타입 + " | 판정 등급: " + 등급;
    }

    // UC5: 길드가입 컨트롤 행위
    public String 길드가입(String 플레이어id, String 길드명, 캐릭터 캐릭터객체, 길드 길드객체) {
        // 1. 필수 플레이어 체크 (Include 관계 실행)
        if (!플레이어검증기.플레이어체크(플레이어id)) {
            return "인증 실패: 플레이어 ID가 올바르지 않습니다.";
        }
        if (캐릭터객체 == null || 길드객체 == null) {
            return "가입 실패: 캐릭터 또는 길드 정보가 누락되었습니다.";
        }

        // 2. 길드 정원 상한선 체크 (get캐릭터리스트크기 호출)
        if (길드객체.get캐릭터리스트크기() >= 5) {
            return "길드 정원 초과: 길드 정원(최대 5명)이 가득 차서 가입할 수 없습니다.";
        }

        // 3. 이미 존재하는 외부 길드 객체에 가입 실무 위임 (캐릭터가입 호출)
        boolean 가입결과 = 길드객체.캐릭터가입(캐릭터객체);
        
        if (!가입결과) {
            return "가입 실패: 이미 해당 길드에 가입되어 있는 캐릭터입니다.";
        }

        return "[" + 길드명 + "] 길드에 정상적으로 가입 처리가 완료되었습니다!";
    }
}